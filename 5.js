let TypeOfPackage = "overnight";
switch (TypeOfPackage) {
  case "overnight":
    console.log("Package will be delivered the next day");
    break;
  case "standard":
    console.log("Package will be delivered in 3-5 days");
    break;
  case "express":
    console.log("Package will be delivered in 1-2 days");
    break;
}
